    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String department= departmentTextField.getText();
        String instructor = instructorTextField.getText();
        String course = courseTextField.getText();
        
        if(departmentTextField.getText().isEmpty()){
            displayText.setText("department ID shouldn't be empty");
            displayText.setForeground(Color.red);
            
        }
        else if(instructorTextField.getText().isEmpty()){
            displayText.setText("instructors  ID shouldn't be empty");
            displayText.setForeground(Color.red);
        }
        else if(courseTextField.getText().isEmpty()){
            displayText.setText("course ID shouldn't be empty");
            displayText.setForeground(Color.red);
        }
        else {
            Connection conn=MySql.ConnectDB();
            Statement stat=null;
            String sql="insert into dep_cour_ins (instructorId , courseId , departmentId)"
                    + "values ('" + instructor + "','" + course+ "','" + department+"')";

            try{
                stat=conn.createStatement();
                stat.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "successfully entered data");
                clearText();

            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

